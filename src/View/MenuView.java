    private void bttSacarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttSacarActionPerformed
        // TODO add your handling code here:
        if (conta == null) {
            JOptionPane.showMessageDialog(this, "Erro: Conta não inicializada.");
            return;
        }

        try {
            double valorSaque = Double.parseDouble(jvalorASacar.getText().replace(",", "."));
            boolean sucesso = conta.sacar(valorSaque);

            if (sucesso) {
                contaController.atualizarConta(conta);
                atualizarSaldo(conta.getSaldo());
                jvalorASacar.setText("0,00"); // Limpar campo após saque
                JOptionPane.showMessageDialog(this, "Valor sacado: " + valorSaque);
            } else {
                JOptionPane.showMessageDialog(this, "Saldo insuficiente.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, insira um valor numérico válido para o saque.");
        }


    }//GEN-LAST:event_bttSacarActionPerformed

    private void bttDepositoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttDepositoActionPerformed
        // TODO add your handling code here:
        if (txtConta.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, insira o número da conta.");
            return;
        }

        if (jvalorADepositar.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, insira o valor a ser depositado.");
            return;
        }

        try {
            int numeroConta = Integer.parseInt(txtConta.getText());
            double valorDeposito = Double.parseDouble(jvalorADepositar.getText().replace(",", "."));

            Conta conta = contaController.buscarContaPorNumero(numeroConta);
            if (conta != null) {
                conta.depositar(valorDeposito);
                contaController.atualizarConta(conta);
                atualizarSaldo(conta.getSaldo());
                jvalorADepositar.setText("0,00"); // Limpar campo após depósito
                JOptionPane.showMessageDialog(this, "Valor depositado: " + valorDeposito);
            } else {
                JOptionPane.showMessageDialog(this, "Conta não encontrada.");
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Por favor, insira valores numéricos válidos para o número da conta e o valor do depósito.");
        }
    }//GEN-LAST:event_bttDepositoActionPerformed

