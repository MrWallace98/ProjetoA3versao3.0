    private void txtSaldocontaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtSaldocontaActionPerformed
        // Obtém o número da conta do cliente
        int numeroConta = Integer.parseInt(txtConta.getText());

        // Busca a conta correspondente usando o ContaController
        Conta conta = contaController.buscarContaPorNumero(numeroConta);

        // Verifica se a conta foi encontrada
        if (conta != null) {
            // Atualiza o saldo no campo txtSaldoconta
            txtSaldoconta.setText(String.format("%.2f", conta.getSaldo()));
        } else {
            JOptionPane.showMessageDialog(this, "Conta não encontrada.");
        }
    }//GEN-LAST:event_txtSaldocontaActionPerformed

